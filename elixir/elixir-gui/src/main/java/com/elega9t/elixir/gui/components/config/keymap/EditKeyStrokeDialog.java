    private void applyButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_applyButtonActionPerformed
        keyStrokeInfo.keyStroke = ((KeyStrokeEditor)keyStrokeTextField).getKeyStroke();
        if(secondKeyStrokeCheckBox.isSelected()) {
            keyStrokeInfo.secondKeyStroke = ((KeyStrokeEditor)secondKeyStrokeTextField).getKeyStroke();
        } else {
            keyStrokeInfo.secondKeyStroke = null;
        }
        setVisible(false);
        dispose();
    }//GEN-LAST:event_applyButtonActionPerformed

