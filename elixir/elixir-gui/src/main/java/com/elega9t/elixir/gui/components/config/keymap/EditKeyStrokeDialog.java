    public static class KeyStrokeInfo {
        
        private KeyStroke keyStroke;
        private KeyStroke secondKeyStroke;

        public KeyStrokeInfo(KeyStrokeInfo keyStrokeInfo) {
            this(keyStrokeInfo.getKeyStroke(), keyStrokeInfo.getSecondKeyStroke());
        }

        public KeyStrokeInfo(KeyStroke keyStroke, KeyStroke secondKeyStroke) {
            this.keyStroke = keyStroke;
            this.secondKeyStroke = secondKeyStroke;
        }

        public KeyStroke getKeyStroke() {
            return keyStroke;
        }

        public KeyStroke getSecondKeyStroke() {
            return secondKeyStroke;
        }

    }
    
